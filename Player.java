public class Player {
  // symbol: Represents the symbol ('X' or 'O') of the player.
  private final char symbol;

  // Initializes the player's symbol.
  public Player(char symbol) {
    this.symbol = symbol;
  }

  // getSymbol(): Returns the player's symbol.
  public char getSymbol() {
    return symbol;
  }
}



 